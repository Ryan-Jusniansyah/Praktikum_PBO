    private void table(){
        DefaultTableModel tbl = (DefaultTableModel) tbpo.getModel();
        tbpo.setRowHeight(40);
        tbpo.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        tbl.setRowCount(0);
        try{
            Connection conn = koneksi.getConnection();
            String sql = "SELECT * FROM tbpo";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            while(rs.next()){
                int id = rs.getInt("IdAntrian");
                String nama = rs.getString("Nama");
                String namasepatu = rs.getString("Namasepatu");
                String Alamat = rs.getString("alamat");
                String nohp = rs.getString("Nohp");
                String ukuran = rs.getString("ukuran");
                int harga = rs.getInt("harga");
                tbl.addRow(new Object[]{id, nama, namasepatu, Alamat, nohp, ukuran, harga});
                tbpo.setModel(tbl);
            }
            conn.close();
        } catch (SQLException e){
            JOptionPane.showMessageDialog(this, "There is an error : " + e.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void table_cari(){
        String keyword = txtSearch.getText();
        DefaultTableModel tbl = (DefaultTableModel) tbpo.getModel();
        tbpo.setRowHeight(40);
        tbpo.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbl.setRowCount(0);
        
        try{
            Connection conn = koneksi.getConnection();
            String sql = "SELECT * FROM tbpo WHERE Nama LIKE '%" + keyword + "%'";
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet rs = statement.executeQuery();
            
            while(rs.next()){
                int id = rs.getInt("IdAntrian");
                String nama = rs.getString("Nama");
                String namasepatu = rs.getString("Namasepatu");
                String Alamat = rs.getString("alamat");
                String nohp = rs.getString("Nohp");
                String ukuran = rs.getString("ukuran");
                int harga = rs.getInt("harga");
                tbl.addRow(new Object[]{id, nama, namasepatu, Alamat, nohp, ukuran, harga});
                tbpo.setModel(tbl);
            }
            conn.close();
        } catch (SQLException e){
            JOptionPane.showMessageDialog(this, "There is an error : " + e.getMessage(), "SQL ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
