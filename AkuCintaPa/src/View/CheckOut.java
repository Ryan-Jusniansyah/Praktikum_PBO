    private void btnCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckOutActionPerformed
        String idtext = txtNoPelanggan.getText();
        String hargatext = txtHarga.getText();
        int id = Integer.parseInt(idtext);
        int harga = Integer.parseInt(hargatext);
        String ukuran = cbUkuran.getSelectedItem().toString();
        try{
            Connection conn = koneksi.getConnection();
            String sql = "INSERT INTO tbpo (IdAntrian, Nama, Namasepatu, alamat, Nohp, ukuran, harga) VALUES (?,?,?,?,?,?,?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setInt(1, id);
            stmt.setString(2, txtNama.getText());
            stmt.setString(3, txtnamasepatu.getText());
            stmt.setString(4, txtAlamat.getText());
            stmt.setString(5, txtNoHp.getText());
            stmt.setString(6, ukuran);
            stmt.setInt(7, harga);
            int masukkan_row = stmt.executeUpdate();
            if(masukkan_row > 0){
                cls();
                JOptionPane.showMessageDialog(this, "Data Successfully Added!", "SQL Information", JOptionPane.INFORMATION_MESSAGE);
                table();
            }
        }catch (SQLException e){
            JOptionPane.showMessageDialog(this, "There is an error : " + e.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCheckOutActionPerformed

